package com.monster.market.bean;

public class SearchPopKeyInfo {

	private String key;
	private String icon;

	public String getKey() {
		return key;
	}

	public void setKey(String key) {
		this.key = key;
	}

	public String getIcon() {
		return icon;
	}

	public void setIcon(String icon) {
		this.icon = icon;
	}

	@Override
	public String toString() {
		return "SearchPopKeyInfo [key=" + key + ", icon=" + icon + "]";
	}

}
